package P1;

import java.util.LinkedHashSet;

public class LinkedHashSetDouble {

	public static void main(String[] args) {
		LinkedHashSet<Double> lh = new LinkedHashSet<Double>();
		lh.add(1.5);
		lh.add(2.5);
		lh.add(3.4);
		lh.add(4.5);
		lh.add(5.5);
		lh.add(6.5);
		lh.add(7.8);
		System.out.println(lh);
	}
}
