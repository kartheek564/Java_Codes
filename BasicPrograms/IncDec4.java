class  IncDec4
{
	public static void main(String[] args) 
	{
		int a=67;
		System.out.println(a++);
		--a;
		System.out.println(++a);
		System.out.println(a+10);
		System.out.println(--a);
		++a;
		a++;
		System.out.println(--a);
		System.out.println(a++);
		System.out.println(++a);
		a--;
		System.out.println(--a);
		System.out.println(a++);
	}	
}
