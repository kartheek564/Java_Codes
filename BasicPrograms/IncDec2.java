class  IncDec2
{
	public static void main(String[] args) 
	{
		int a=34;
		System.out.println(a++);//34
		System.out.println(--a);//34
		System.out.println(++a);//35
		System.out.println(a++);//35
		System.out.println(--a);//35
		System.out.println(a--);//35
		System.out.println(a++);//34
		System.out.println(--a);//34
		System.out.println(++a);//35
	}
}
