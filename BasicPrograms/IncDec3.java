class  IncDec3
{
	public static void main(String[] args) 
	{
		int m=34;
		int n=78;
		System.out.println(++m);//35
		System.out.println(--m);//34
		System.out.println(n++);//78-79
		System.out.println(++m);//35
		System.out.println(m++);//35-36
		System.out.println(n--);//79-78
		System.out.println(++n);//79
		System.out.println(--m);//35
		System.out.println(m+10);//45
		System.out.println(m+n);//114
		System.out.println(n+10);//89
		System.out.println(--m);//34

	}	
}
