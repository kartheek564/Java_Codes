class Switch3
{
	public static void main(String[] args) 
	{
		switch(9)
		{
			
			case 1: System.out.println("Haii"); 
			case 2: System.out.println("Hello"); 
			default: System.out.println("Bye bye");break;
			case 3: System.out.println("Good Evening"); 
			case 4: System.out.println("Tata"); 
			case 5: System.out.println("See You"); 
			
		}
		
	}
}
