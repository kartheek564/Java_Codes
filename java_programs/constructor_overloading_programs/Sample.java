class Sample 
{
	public Sample(int i)
	{
		System.out.println("Execute sample constructor with int arg");
		System.out.println("i :"+i);
	}
	public Sample(double i)
	{
		System.out.println("Execute sample constructor with double arg");
		System.out.println("i :"+i);
	}
	public Sample(String a,boolean b)
	{
		System.out.println("Execute sample constructor with String and boolean arg");
		System.out.println("a :"+a);
		System.out.println("b :"+b);
	}
}
